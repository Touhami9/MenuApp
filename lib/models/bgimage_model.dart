class FloatingBgImage {
  final String imagePath;
  final double top;
  final double? left;
  final double width;
  final double height;
  final double opacity;
  final double dx;
  final double dy;

  FloatingBgImage({
    required this.imagePath,
    required this.top,
    required this.left,
    required this.width,
    required this.height,
    required this.opacity,
    required this.dx,
    required this.dy,
  });
}
